// Relational operators
// These operators compare two values and return a boolean result
// Examples include: >, <, >=, <=, ==, !=, ===, !==
var a = 5 
var b = 6 
console.log("Is a greater than b?", a > b); // false
console.log("Is a less than b?", a < b); // true


